void adcInit()
{
    ADC_Start();
    Cy_SAR_StartConvert( SAR, CY_SAR_START_CONVERT_CONTINUOUS );
}

void getForceValues( uint8_t values[] )
{
    for( int i = 0; i < 8; i++ )
    {
        values[i] = Cy_SAR_GetResult16( SAR, i );
    }
}
